#include "libft.h"

char	*ft_convert(int n, char *str, int i)
{
	if (n == INTMIN)
		str = ft_strdup("-2147483648");
	else
	{
		if (n < 0)
		{
			n *= -1;
			str[0] = '-';
			while (--i > 0)
			{
				str[i] = (n % 10 + '0');
				n = n / 10;
			}
		}
		else
		{
			while (--i >= 0)
			{
				str[i] = (n % 10 + '0');
				n = n / 10;
			}
		}
	}
	return (str);
}
